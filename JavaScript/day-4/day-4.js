function Calculator(num1, num2){
    console.log(num1 + num2 + " Addition ");
    console.log(num1 - num2 + " Substraction " );
    console.log(num1 * num2 +" Multiplication ");
    console.log(num1 / num2 + " Division");
    console.log(num1 % num2 + "Modulo");
}
var num1 = 10;
let num2 = 5;
